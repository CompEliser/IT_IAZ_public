# Завдання 5. Знайти найбільший спільний дільник двох чисел.
# 1) Запис алгоритму розрахунку
def gcd(a, b):
    while b:
        a, b = b, a % b
    return a
# 2) Введення двох чисел користувачем
num1 = int(input("Введіть перше число: "))
num2 = int(input("Введіть друге число: "))
# 3) Знаходження найбільшого спільного дільника
result = gcd(num1, num2)
# 4) Відображення результату
print("Найбільший спільний дільник:", result)

# Завдання 6. Перевести температуру з Цельсія в Фаренгейти.
# 1) Введення температури в градусах Цельсія користувачем
celsius = float(input("Введіть температуру в градусах Цельсія: "))
