# Завдання 7. Порахувати факторіал заданого числа.
# 1 етап: Описати функцію для розрахунку факторіала
def factorial(n):
    if n == 0 or n == 1:
        return 1
    else:
        return n * factorial(n - 1)
# 2 етап: Введення числа користувачем
num = int(input("Введіть число: "))
# 3 етап: Розрахунок факторіала
result = factorial(num)
# 4 етап: Виведення результату
print("Факторіал числа", num, "дорівнює", result)

